package pattern;

public class ques16 {
    public static void main(String[] args) {
        int n=5;
        int sum=1;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print((char) (64+sum)+" ");
                sum+=1;
            }
            System.out.println();
        }
    }
}
